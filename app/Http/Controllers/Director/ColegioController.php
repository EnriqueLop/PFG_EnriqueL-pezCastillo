       * @param  \Illuminate\Http\Request  $request
       * @return \Illuminate\Http\Response
       */
      public function store(Request $request)
      {
        $request->validate([
            'centro'=>'required',
            'name' => 'required',
        ]);

        $colegio = Colegio::find(Auth::user()->colegio_id);
        $colegio -> name = $request -> centro . ' ' . $request -> name;
        $colegio -> save();

        return redirect('/login');
      }


